-- ============================================================
-- Sync Runs — Track every QB sync execution
-- ============================================================
CREATE TABLE IF NOT EXISTS sync_runs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  sync_type TEXT NOT NULL
    CHECK (sync_type IN ('scheduled_weekly', 'scheduled_daily', 'manual')),
  started_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  completed_at TIMESTAMPTZ,
  status TEXT NOT NULL DEFAULT 'running'
    CHECK (status IN ('running', 'success', 'error')),
  since_date DATE,
  entities_fetched JSONB,            -- {purchases: 42, deposits: 10, transfers: 5, journal_entries: 2}
  entities_new INTEGER DEFAULT 0,
  entities_updated INTEGER DEFAULT 0,
  error_message TEXT,
  triggered_by TEXT,                  -- 'cron', 'admin:rahul@email.com'
  created_at TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_sync_runs_status ON sync_runs(status);
CREATE INDEX IF NOT EXISTS idx_sync_runs_completed ON sync_runs(completed_at DESC);

ALTER TABLE sync_runs ENABLE ROW LEVEL SECURITY;
DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'sync_runs' AND policyname = 'Authenticated read sync_runs') THEN
    CREATE POLICY "Authenticated read sync_runs" ON sync_runs FOR SELECT TO authenticated USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'sync_runs' AND policyname = 'Service write sync_runs') THEN
    CREATE POLICY "Service write sync_runs" ON sync_runs FOR ALL TO service_role USING (true) WITH CHECK (true);
  END IF;
END $$;

-- ============================================================
-- Integrity Findings — Dynamic data quality issues
