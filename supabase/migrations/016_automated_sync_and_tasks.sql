-- ============================================================
-- QB Writeback Queue — Proposed changes to push back to QB
-- ============================================================
CREATE TABLE IF NOT EXISTS qb_writeback_queue (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  qb_entity_type TEXT NOT NULL,        -- 'Purchase', 'Deposit', 'JournalEntry', etc.
  qb_entity_id TEXT NOT NULL,
  field_name TEXT NOT NULL,            -- 'PrivateNote', 'AccountRef', 'Line', etc.
  old_value TEXT,
  new_value TEXT NOT NULL,
  reason TEXT,
  status TEXT NOT NULL DEFAULT 'proposed'
    CHECK (status IN ('proposed', 'approved', 'executed', 'failed', 'rejected')),
  approved_by TEXT,
  approved_at TIMESTAMPTZ,
  executed_at TIMESTAMPTZ,
  error_message TEXT,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now()
);

