-- ============================================================
-- Todos — Human task queue generated by AI + integrity checks
-- ============================================================
CREATE TABLE IF NOT EXISTS todos (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  title TEXT NOT NULL,
  description TEXT,
  source TEXT NOT NULL DEFAULT 'manual'
    CHECK (source IN ('integrity_check', 'ai_categorization', 'receipt_mismatch', 'sync_error', 'manual')),
  source_entity_type TEXT,             -- 'integrity_finding', 'qb_transaction', 'receipt', 'sync_run'
  source_entity_id UUID,
  priority TEXT NOT NULL DEFAULT 'medium'
    CHECK (priority IN ('urgent', 'high', 'medium', 'low')),
  status TEXT NOT NULL DEFAULT 'open'
    CHECK (status IN ('open', 'in_progress', 'done', 'auto_resolved', 'dismissed')),
  assigned_to TEXT DEFAULT 'owner'
    CHECK (assigned_to IN ('owner', 'bookkeeper', 'ai')),
  due_date DATE,
  resolved_at TIMESTAMPTZ,
  resolved_by TEXT,
  resolution_notes TEXT,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_todos_status ON todos(status);
CREATE INDEX IF NOT EXISTS idx_todos_priority ON todos(priority);
CREATE INDEX IF NOT EXISTS idx_todos_assigned ON todos(assigned_to);
CREATE INDEX IF NOT EXISTS idx_todos_open ON todos(status, priority) WHERE status IN ('open', 'in_progress');
CREATE INDEX IF NOT EXISTS idx_todos_source ON todos(source_entity_type, source_entity_id);

